#include "ft_printf.h"

void	ft_putstr(char *s, int *j)
{
	int	nbr;

	nbr = 0;
	if (!s)
	{
		*j = *j + 6;
		write(1, "(null)", 6);
		return ;
	}
	while (s[nbr])
	{
		write(1, &s[nbr], 1);
		*j = *j + 1;
		nbr++;
	}
}
